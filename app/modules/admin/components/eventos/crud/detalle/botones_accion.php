    }
}

function confirmarEliminarEvento(eventoId) {
    if (confirm('¿Está seguro de que desea eliminar este evento? Esta acción no se puede deshacer.')) {
        window.location.href = `<?= URL_PATH ?>admin/eliminarEvento/${eventoId}`;
    }
}
